    @Override
     public void gameWin(){
         if(player2hp<=0||player1hp<player2hp&&counter==0){
             player2hp=0;
             pgPlayer2.setValue(player2hp);
             setFalse();
             whenWin();
             JOptionPane.showMessageDialog(null, "Player 1 Wins","Congratulations",JOptionPane.INFORMATION_MESSAGE); 
             WinMenu();
             
         }else if(player1hp<=0){
             player1hp=0;
             pgPlayer1.setValue(player1hp);
             setFalse();
             whenWin();
             JOptionPane.showMessageDialog(null, "Player 2 Wins");
             WinMenu();
             
             
         }
     }
     
     @Override
     public void timer(){
         Thread time=new Thread(){
           public void run(){
               while(checker==1){
                   if(pause==0){
                       counter2--;
                       counter2--;
                   if(counter2==0){ 
                           counter--;
                           lblTime.setText(""+counter);   
                           if(counter==25){
                            if(turncount==1){
                            turncount=2;
                            turn();
                            checker=1;
                            
                        }else if(turncount==2){
                            turncount=1;  
                            turn();
                            checker=1;
                                
                        }
                       }
                       
                      }
                       
                   }
                    
               }
           }
       };
         
       time.start();
       
     }
     
     public void playerHp(){
         if(turncount==0){
             pgPlayer1.setValue(player1hp);
             pgPlayer2.setValue(player2hp);
             turncount=1;
             turn();
             
         }
         else if(turncount==1){
             pgPlayer1.setValue(player1hp);
             gameWin();
            
             
         }else if(turncount==2){
             pgPlayer2.setValue(player2hp);
             gameWin();
              
         }
     }
